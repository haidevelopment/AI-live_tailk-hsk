export function buildSystemInstruction(level, topicId) {
  const levelKey = `HSK${level}`;
  const levelPrompt = HSK_PROMPTS[levelKey] || HSK_PROMPTS.HSK1;
  const topic = TOPIC_PROMPTS[topicId];

  let instruction = levelPrompt.systemInstruction;

  if (topic) {
    instruction += `\n\n今天的话题：${topic.zh}\n话题说明：${topic.context}`;
  }

  return instruction;
}

export function getGreeting(level, topicId) {
  const levelKey = `HSK${level}`;
  const levelPrompt = HSK_PROMPTS[levelKey] || HSK_PROMPTS.HSK1;
  const topic = TOPIC_PROMPTS[topicId];

  if (topic) {
    return `你好！今天我们练习"${topic.zh}"这个话题。准备好了吗？`;
  }

  return levelPrompt.greeting;
}
